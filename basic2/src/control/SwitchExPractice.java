package control;

public class SwitchExPractice {

	public static void main(String[] args) {
		
		switch(1) {
			case 1:
				System.out.println("icecream");
			break;
			case 2:
				System.out.println("macaron");
			break;
			case 3:
				System.out.println("cookie");
			break;
			case 4:
				System.out.println("candy");
			break;
			default:
				System.out.println("메뉴를 입력해주세요");
				
				
		}

	}

}

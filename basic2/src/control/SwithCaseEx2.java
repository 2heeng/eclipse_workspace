package control;

public class SwithCaseEx2 {

	public static void main(String[] args) {
		
		int menu=2;
		
		switch(menu) {
			case 1 ->{
				System.out.println("짜장");
			}
			case 2 ->{
				System.out.println("짬뽕");
			}
			case 3 ->{
				System.out.println("볶음밥");
			}
			default->{
				System.out.println("주문하실 메뉴의 번호를 확인해주세요");
			}
			}

	}

}
